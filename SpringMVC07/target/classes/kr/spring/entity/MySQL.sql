CREATE TABLE TBLMEMBER(
	MEMID VARCHAR(50) NOT NULL,
	MEMPW VARCHAR(50) NOT NULL,
	MEMNAME VARCHAR(50) NOT NULL,
	MEMPHONE VARCHAR(50) NOT NULL,
	MEMADDR VARCHAR(100),
	LATITUDE DECIMAL(13, 10),  -- 현재 위치 위도 --
	LONGITUDE DECIMAL(13, 10),  -- 현재 위치 경도 --
	PRIMARY KEY(MEMID)
